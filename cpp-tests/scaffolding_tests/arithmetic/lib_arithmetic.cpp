#include "lib_arithmetic.hpp"

uint64_t lib_arithmetic::add(uint64_t a, uint64_t b) {
    return a + b;
}

uint64_t lib_arithmetic::sub(uint64_t a, uint64_t b) {
    return a - b;
}

uint64_t lib_arithmetic::div(uint64_t a, uint64_t b) {
    return a / b;
}

int8_t lib_arithmetic::equal(uint64_t a, uint64_t b) {
    return a == b;
}
